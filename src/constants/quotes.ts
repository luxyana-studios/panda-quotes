export const RED_PANDA_QUOTES: string[] = CATEGORIZED_QUOTES.map((q) => q.text);

export function getQuoteForCategories(categories: string[]): string {
  let pool: CategorizedQuote[];

  if (categories.length === 0) {
    pool = CATEGORIZED_QUOTES;
  } else {
    pool = CATEGORIZED_QUOTES.filter((q) =>
      q.categories.some((c) => categories.includes(c)),
    );
    if (pool.length === 0) {
      pool = CATEGORIZED_QUOTES;
    }
  }

  return pool[Math.floor(Math.random() * pool.length)].text;
}
